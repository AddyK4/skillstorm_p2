@RestController
@RequestMapping("/plans")
@CrossOrigin(origins = "*")
public class PlanController {
	
	@Autowired
	private PlanRepository repository; // inject the EmployeeService instead
	
	@PostMapping(value = "/plan")
	public ResponseEntity<Plan> save(@RequestBody @Valid Plan plan) {
		Plan body = repository.save(plan);
		return new ResponseEntity<>(body, HttpStatus.CREATED);
	}


}
